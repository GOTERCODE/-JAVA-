package Pt0321;

public class CompareOperatorExample {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 10;
		boolean result1 = (num1 == num2);
		boolean result2 = (num1 != num2);
		boolean result3 = (num1 <= num2);
		
		System.out.println("result1 : " + result1);			//num1(10)은 num2(10)과 같은지
		System.out.println("result2 : " + result2);			//num1(10)은 num2(10)과 다른지
		System.out.println("result3 : " + result3);			//num1(10)은 num2(10)보다 작거나 같은지
		
		char char1 = 'A';
		char char2 = 'B';
		boolean result4 = (char1 < char2);
		System.out.println("result4 : " + result4);			//char1(아스키코드 A)는 char2(아스키코드 B)보다 작은지

	}

}
